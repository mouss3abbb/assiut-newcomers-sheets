#include<bits/stdc++.h>
using namespace std;

int main(){

    // Note to use long long to avoid integer overflow while multiplying
    long long x,y;
    cin>>x>>y;

    cout<<x<<" + "<<y<<" = "<<x+y<<"\n";
    cout<<x<<" * "<<y<<" = "<<x*y<<"\n";
    cout<<x<<" - "<<y<<" = "<<x-y<<"\n";
    return 0;
}